
public class MathOperators {
	public static void main(String[] args){
		//takes the absolute value of the float, int, double, etc.
		System.out.println(Math.abs(-7.4));
		
		//always rounds up.
		System.out.println(Math.ceil(7.4));
		
		//always rounds down.
		System.out.println(Math.floor(7.4));
		
		//finds the bigger of ONLY 2 numbers.
		System.out.println(Math.max(7.4, 5.2));
		
		//finds the smaller of 2
		System.out.println(Math.min(8.6, 7.4));
		
		//takes the first argument and puts it to the power of the second arg.
		System.out.println(Math.pow(5, 2));
		//Five squared
		
		//takes the square root.
		System.out.println(Math.sqrt(9));
	}

}
